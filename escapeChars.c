
#include <stdio.h>

int main() {
    // Escape characters in C
    printf("Hello, World!\n");  // Newline character
    printf("Tab\tSpace\n");      // Tab character
    printf("Backslash: \\\n");   // Backslash character
    printf("Single quote: \'\n"); // Single quote character
    printf("Double quote: \"\n"); // Double quote character
    printf("Backspace\bTest\n"); // Backspace character
    printf("Form feed\fTest\n"); // Form feed character
    printf("Carriage return\rTest\n"); // Carriage return character
    printf("Alert sound\a\n");   // Alert (bell) character
    printf("Vertical tab\vTest\n"); // Vertical tab character

    return 0;
}